CREATE TABLE IF NOT EXISTS public.finance_assumptions_ledger (
    id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
    user_id UUID NOT NULL,
    assumption_type TEXT NOT NULL CHECK (assumption_type IN ('reporting_rule', 'scenario_assumption', 'benchmark_set')),
    assumption_key TEXT NOT NULL,
    label TEXT NOT NULL,
    scope TEXT NOT NULL DEFAULT 'global',
    value JSONB NOT NULL DEFAULT '{}'::jsonb,
    summary TEXT NOT NULL,
    notes TEXT,
    source TEXT NOT NULL DEFAULT 'manual',
    effective_start TIMESTAMPTZ,
    effective_end TIMESTAMPTZ,
    is_active BOOLEAN NOT NULL DEFAULT true,
    created_at TIMESTAMPTZ NOT NULL DEFAULT now(),
    updated_at TIMESTAMPTZ NOT NULL DEFAULT now()
);

CREATE INDEX IF NOT EXISTS idx_finance_assumptions_ledger_user_type
    ON public.finance_assumptions_ledger(user_id, assumption_type, created_at DESC);

CREATE INDEX IF NOT EXISTS idx_finance_assumptions_ledger_user_scope
    ON public.finance_assumptions_ledger(user_id, scope, created_at DESC);

ALTER TABLE public.finance_assumptions_ledger ENABLE ROW LEVEL SECURITY;

DROP POLICY IF EXISTS "Users can view own finance assumptions" ON public.finance_assumptions_ledger;
DROP POLICY IF EXISTS "Users can insert own finance assumptions" ON public.finance_assumptions_ledger;
DROP POLICY IF EXISTS "Users can update own finance assumptions" ON public.finance_assumptions_ledger;
DROP POLICY IF EXISTS "Users can delete own finance assumptions" ON public.finance_assumptions_ledger;
DROP POLICY IF EXISTS "Service role manages finance assumptions" ON public.finance_assumptions_ledger;

CREATE POLICY "Users can view own finance assumptions"
ON public.finance_assumptions_ledger FOR SELECT TO authenticated
USING ((SELECT auth.uid()) = user_id);

CREATE POLICY "Users can insert own finance assumptions"
ON public.finance_assumptions_ledger FOR INSERT TO authenticated
WITH CHECK ((SELECT auth.uid()) = user_id);

CREATE POLICY "Users can update own finance assumptions"
ON public.finance_assumptions_ledger FOR UPDATE TO authenticated
USING ((SELECT auth.uid()) = user_id)
WITH CHECK ((SELECT auth.uid()) = user_id);

CREATE POLICY "Users can delete own finance assumptions"
ON public.finance_assumptions_ledger FOR DELETE TO authenticated
USING ((SELECT auth.uid()) = user_id);

CREATE POLICY "Service role manages finance assumptions"
ON public.finance_assumptions_ledger FOR ALL TO service_role
USING (true)
WITH CHECK (true);

