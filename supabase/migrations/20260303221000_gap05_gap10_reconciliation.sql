DROP POLICY IF EXISTS "Users can view workflow marketplace reviews_select" ON public.workflow_template_marketplace_reviews;

CREATE POLICY "Users can view active workflow marketplace reviews"
ON public.workflow_template_marketplace_reviews FOR SELECT TO public
USING (
    EXISTS (
        SELECT 1
        FROM public.workflow_template_marketplace listing
        WHERE listing.id = listing_id
          AND listing.is_active = true
    )
);
